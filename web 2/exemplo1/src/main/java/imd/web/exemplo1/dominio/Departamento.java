package imd.web.exemplo1.dominio;

public class Departamento {
	
	private String nome;
	
	private Chefe chefe;

	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}

	/**
	 * @return the chefe
	 */
	public Chefe getChefe() {
		return chefe;
	}

	/**
	 * @param chefe the chefe to set
	 */
	public void setChefe(Chefe chefe) {
		this.chefe = chefe;
	}
	
	

}
